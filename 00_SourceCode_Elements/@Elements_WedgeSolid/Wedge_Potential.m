
function PE=Wedge_Potential(obj,X,Kwedge,L0ref)

    % the input X is a matrix that stores the nodal coordinates of 6 nodes
    % it should be 6 by 3

    PE=1/2*Kwedge*((norm(X(1,:)-X(2,:))-L0ref(1))^2+...
        (norm(X(2,:)-X(3,:))-L0ref(2))^2+...
        (norm(X(1,:)-X(3,:))-L0ref(3))^2+...
        (norm(X(4,:)-X(5,:))-L0ref(4))^2+...
        (norm(X(5,:)-X(6,:))-L0ref(5))^2+...
        (norm(X(4,:)-X(6,:))-L0ref(6))^2+...
        (norm(X(1,:)-X(4,:))-L0ref(7))^2+...
        (norm(X(2,:)-X(5,:))-L0ref(8))^2+...
        (norm(X(3,:)-X(6,:))-L0ref(9))^2+...
        (norm(X(1,:)-X(5,:))-L0ref(10))^2+...
        (norm(X(4,:)-X(2,:))-L0ref(11))^2+...
        (norm(X(2,:)-X(6,:))-L0ref(12))^2+...
        (norm(X(5,:)-X(3,:))-L0ref(13))^2+...
        (norm(X(3,:)-X(4,:))-L0ref(14))^2+...
        (norm(X(6,:)-X(1,:))-L0ref(15))^2);    
    
end